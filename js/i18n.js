function switchLanguage(lang) {
  try {
    window.currentLang = lang;
    localStorage.setItem('preferredLanguage', lang); // Simpan pilihan lang

    // Toggle buttons
    document.getElementById('btn-lang-id')?.classList.toggle('active', lang === 'id');
    document.getElementById('btn-lang-en')?.classList.toggle('active', lang === 'en');

  // Simple translate by data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    // Check if key exists for the current language
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    } else if (lang === 'en' && translations.en[key]) {
      el.innerHTML = translations.en[key];
    } else {
      // If lang is 'id' and no key, maybe the key is actually the ID text (old style)
      // Otherwise, keep as is or fallback to key
      // If it's a semantic key like rule_1, we must have it translated
    }
  });

  // Placeholder translation
  document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
    if (!el.hasAttribute('data-i18n-ph')) {
      el.setAttribute('data-i18n-ph', el.placeholder);
    }
    const key = el.getAttribute('data-i18n-ph');
    if (lang === 'en' && translations.en[key]) {
      el.placeholder = translations.en[key];
    } else {
      el.placeholder = key;
    }
  });
  
  // Labels inside form-group that don't have span data-i18n yet (auto tagger)
  document.querySelectorAll('.form-label').forEach(el => {
    // Only touch text nodes so we don't destroy <span class="required">*</span>
    Array.from(el.childNodes).forEach(node => {
      if (node.nodeType === 3) { // Text node
        const originalText = node.textContent.trim();
        if (originalText && originalText.length > 2) {
          if (!el.hasAttribute('data-i18n-auto')) {
             el.setAttribute('data-i18n-auto', originalText);
          }
          const key = el.getAttribute('data-i18n-auto');
          if (lang === 'en' && translations.en[key]) {
            node.textContent = translations.en[key] + ' ';
          } else {
            node.textContent = key + ' ';
          }
        }
      }
    });
  });

  // Select Options Auto Tagger
  document.querySelectorAll('option').forEach(el => {
    if (!el.hasAttribute('data-i18n-opt')) {
      el.setAttribute('data-i18n-opt', el.textContent.trim());
    }
    const key = el.getAttribute('data-i18n-opt');
    if (lang === 'en' && translations.en[key]) {
      el.textContent = translations.en[key];
    } else {
      el.textContent = key;
    }
  });
  
  // Specific Buttons
  const submitTextSpan = document.querySelector('#submit-btn .btn-text');
  if (submitTextSpan) {
    if (!submitTextSpan.hasAttribute('data-i18n-btn')) {
      submitTextSpan.setAttribute('data-i18n-btn', "Lanjut ke Video Induksi →");
    }
    const key = submitTextSpan.getAttribute('data-i18n-btn');
    if (lang === 'en' && translations.en[key]) {
      submitTextSpan.textContent = translations.en[key];
    } else {
      submitTextSpan.textContent = "Lanjut ke Video Induksi →";
    }
  }

  } catch (err) {
    console.error("Language Switch Error:", err);
  }
}

// Auto-initialize on load
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('preferredLanguage') || 'id';
  switchLanguage(savedLang);
});

// Expose to window for HTML onclick handlers
window.switchLanguage = switchLanguage;
